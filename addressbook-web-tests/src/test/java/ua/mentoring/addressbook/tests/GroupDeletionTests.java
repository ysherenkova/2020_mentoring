package ua.mentoring.addressbook.tests;// Generated by Selenium IDE
import org.hamcrest.CoreMatchers;
import org.hamcrest.MatcherAssert;
import org.testng.Assert;
import org.testng.annotations.BeforeMethod;
import org.testng.annotations.Test;
import ua.mentoring.addressbook.model.GroupData;
import ua.mentoring.addressbook.model.Groups;

import java.util.List;
import java.util.Set;

import static org.hamcrest.CoreMatchers.equalTo;
import static org.hamcrest.MatcherAssert.assertThat;
import static org.testng.Assert.assertEquals;


public class GroupDeletionTests extends TestBase {

  @BeforeMethod
  public void ensurePreconditions() {
    applicationManager.goTo().groupPage();
    if (applicationManager.group().all().size() == 0) {
      applicationManager.group().create(new GroupData().withName("Test group"));
    }
  }

  @Test
  public void testGroupDeletion () {
    Groups before = applicationManager.group().all();
    GroupData deletedGroup = before.iterator().next();
    applicationManager.group().delete(deletedGroup);
    assertThat(applicationManager.group().count(), equalTo(before.size() -1));
    Groups after = applicationManager.group().all();
    assertThat(after, equalTo(before.without(deletedGroup)));
  }



}